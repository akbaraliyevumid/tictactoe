namespace TicTacToe
{
    /// <summary>
    /// The type of value a cell in the game is currently at
    /// </summary>
    public enum MarkType
    {
        /// <summary>
        /// The cell hasn't been clicked yet
        /// </summary>
        FREE,

        /// <summary>
        /// The cell is a O
        /// </summary>
        NOUGHT,

        /// <summary>
        /// The cell is a X
        /// </summary>
        CROSS
    }
}
